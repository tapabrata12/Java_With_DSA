package Basics_of_JAVA.Topic_2_Data_type_and_Oporators;

public class Arithmetic_int_operations {
    public static void main(String[] args) {
        // The 4 common arithmetic operation and it's operators
        int x = 5; // We can also write / Declare variables in this way
        int y = 2; // We can also write / Declare variables in this way
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y); // As two variable are integer in type that's why reminder part is obsoleted and only the Quotient part is printed if it has decimal datatype then answer was a decimal number

        System.out.println(y+x);
        System.out.println(y-x);
        System.out.println(y*x);
        System.out.println(y/x); // Answer will be zero

        // We can also do the with fewer lines
        // int x = 5, y = 2;

    }
}